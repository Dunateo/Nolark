/**
* Fonction qui retourne l'alcool pur ingéré en fonction du nombre de verre
*
* @param {int} nbVerres
* @returns {int}
*/
function getAlcoolPur(nbVerres) {
 const uniteAlcool = 10;
 return uniteAlcool * nbVerres;
}
